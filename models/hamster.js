class Hamster {
  constructor(id, name, age, favFood, loves, imgName, wins, defeats, games ) {
    this.id = id;
    this.name = name;
    this.age = age;
    this.favFood = favFood;
    this.loves = loves;
    this.imgName = imgName;
    this.wins = wins;
    this.defeats = defeats;
    this.games = games;
  }
}

module.exports = Hamster;